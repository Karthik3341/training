package com.chainsys.day5;

public class LeapYearOrNot {

	public static void main(String[] args) {
int year=1956;
for(int i=0;i<=0;i++)
{
	if(year%400==0)
	{
		System.out.println("The given year is leap");
	}
	else if(year%100 ==0) {
		System.out.println("The given year is not leap");
	}
	else
	{
		System.out.println("The given year is not leap");
	}
}
	}

}
